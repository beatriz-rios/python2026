notas_turma = [4, 7, 8, 5, 10, 9, 6, 8, 7, 10]

menor_nota = min(notas_turma)
maior_nota = max(notas_turma)
soma_notas = sum(notas_turma)
media_notas = soma_notas / len(notas_turma)

print(f"A menor nota é {menor_nota}")
print(f"A maior nota é {maior_nota}")
print(f"A média das notas é {media_notas}")